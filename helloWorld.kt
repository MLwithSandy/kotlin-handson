fun main(){
    val name = "Kotlin"

    println("Hello World! ${name}")

    // ++ and -- operators
    var x = 10
    println(x) // 10
    println(x++) // 10
    println(x) // 11
    println(++x) // 12
    println(x) // 12
    println(--x) // 11
    println(x) // 11
    println(x--) // 11
    println(x) // 10

}
